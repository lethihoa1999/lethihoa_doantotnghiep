                <h4 class="color-h">1. Theo dõi sau tiêm chủng</h4>
                <div class="color-h" style="font-size: 16px;">Trẻ em và người lớn đều cần được theo dõi tối thiểu 30 phút tại cơ sở tiêm chủng. Nếu phát hiện các biểu hiện bất thường, 
                nôn trớ, thở nhanh hay ngắt quãng, thở khò khè, da mẩn đỏ,… cần báo ngay cho nhân viên y tế gần nhất.<br>
                    <br>Trẻ em cần tiếp tục được theo dõi trong 24 – 48 giờ sau khi tiêm bao gồm:
                    <br>- Thân nhiệt, nhịp thở
                    <br>- Sự tỉnh táo (chơi đùa), ăn, ngủ
                    <br>- Quan sát da toàn thân và vùng tiêm (sưng, mẩm đỏ, phát ban)
                </div>
                <h4 class="color-h">2. Chăm sóc trẻ sau tiêm chủng</h4>
                <div class="color-h" style="font-size: 16px;">- Cho trẻ mặc quần áo thoáng mát
                    <br>- Duy trì chế độ dinh dưỡng hàng ngày, cho trẻ bú mẹ và uống nước nhiều hơn
                    <br>- Có thể dùng thuốc hạ sốt thông thường (paracetamol, ibuprofen) với liều phù hợp cân nặng khi trẻ sốt > 38.5oC, quấy khóc.
                    <br>- Nếu tại vết tiêm sưng, đỏ, có thể chườm lạnh để giúp giảm đau và giảm sưng cho trẻ
                    <br>- Khi bế trẻ tránh chạm vào vết tiêm, không xoa dầu, chườm nóng, nặn chanh, đắp khoai tây hay bôi đắp bất cứ thứ gì lên vết tiêm vì có thể gây nhiễm trùng vết tiêm
                    <br>- Không dùng aspirin, không dùng thêm các thuốc ho và hạ sốt khác vì các chế phẩm này có thể làm tăng liều paracetamol ở trẻ
